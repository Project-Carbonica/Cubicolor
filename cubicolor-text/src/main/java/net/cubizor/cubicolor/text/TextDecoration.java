package net.cubizor.cubicolor.text;

/**
 * Platform-agnostic text decorations
 */
public enum TextDecoration {
    /**
     * Bold text
     */
    BOLD,

    /**
     * Italic text
     */
    ITALIC,

    /**
     * Underlined text
     */
    UNDERLINED,

    /**
     * Strikethrough text
     */
    STRIKETHROUGH,

    /**
     * Obfuscated/magic text
     */
    OBFUSCATED
}